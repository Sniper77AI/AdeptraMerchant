function woocommerceTs(): string {
  return `/**
 * Thin WooCommerce Store API client. Catalog reads + cart building ONLY —
 * this file never calls /wc/store/v1/checkout (payment) or any /wc/v3/
 * (admin) route.
 *
 * Cart state persists across calls via the Cart-Token header (the documented
 * headless pattern): the first cart-touching request returns a Cart-Token
 * response header, cached here in memory and replayed on every subsequent
 * call. When using Cart-Token, no separate nonce is required.
 *
 * One in-memory cart token per running process — this scaffold assumes one
 * server process serves one shopper session at a time (the typical MCP
 * stdio deployment model: one process per connected agent session). A
 * multi-shopper deployment would need a per-session token store instead.
 */

const STORE_URL = process.env.WOOCOMMERCE_STORE_URL;
if (!STORE_URL) {
  throw new Error("Set WOOCOMMERCE_STORE_URL in .env (see .env.example).");
}

const BASE = \`\${STORE_URL.replace(/\\/+$/, "")}/wp-json/wc/store/v1\`;

let cartToken: string | null = null;

async function storeApiFetch(path: string, init: RequestInit = {}): Promise<any> {
  const headers: Record<string, string> = {
    "content-type": "application/json",
    ...(init.headers as Record<string, string> | undefined),
  };
  if (cartToken) headers["Cart-Token"] = cartToken;

  const res = await fetch(\`\${BASE}\${path}\`, { ...init, headers });
  const token = res.headers.get("Cart-Token");
  if (token) cartToken = token;

  const text = await res.text();
  if (!res.ok) {
    throw new Error(\`WooCommerce Store API \${init.method ?? "GET"} \${path} -> \${res.status}: \${text}\`);
  }
  return text ? JSON.parse(text) : null;
}

export interface Product {
  id: number;
  name: string;
  sku: string;
  permalink: string;
  short_description: string;
  is_in_stock: boolean;
  prices: { price: string; currency_code: string; currency_minor_unit: number };
}

export async function searchProducts(query: string, perPage = 10): Promise<Product[]> {
  const q = new URLSearchParams({ search: query, per_page: String(perPage) });
  return storeApiFetch(\`/products?\${q.toString()}\`);
}

export async function getProduct(id: number): Promise<Product> {
  return storeApiFetch(\`/products/\${id}\`);
}

export interface CartItem {
  key: string;
  id: number;
  name: string;
  quantity: number;
  prices: { price: string };
}

export interface Cart {
  items: CartItem[];
  totals: Record<string, unknown>;
}

export async function getCart(): Promise<Cart> {
  return storeApiFetch("/cart");
}

export async function addToCart(id: number, quantity: number): Promise<Cart> {
  return storeApiFetch("/cart/add-item", { method: "POST", body: JSON.stringify({ id, quantity }) });
}

export async function updateCartItem(key: string, quantity: number): Promise<Cart> {
  return storeApiFetch("/cart/update-item", { method: "POST", body: JSON.stringify({ key, quantity }) });
}

export async function removeCartItem(key: string): Promise<Cart> {
  return storeApiFetch("/cart/remove-item", { method: "POST", body: JSON.stringify({ key }) });
}
`;
}

