export function generateMcpScaffoldArtifact(ctx: ArtifactContext): ArtifactDraft | null {
  if (ctx.platform !== "woocommerce") return null; // known-platform gate, never guessed

  const sig = byKey(ctx.signals);
  const checkout = sig.get("capability_checkout_declared");
  const cart = sig.get("capability_cart_declared");
  const catalog = sig.get("capability_catalog_declared");

  const allAlreadyPassing = [checkout, cart, catalog].every((s) => s?.status === "pass");
  if (allAlreadyPassing) return null; // agent-shopability already declared+working; nothing to add

  const files: ArtifactFile[] = [
    { path: "README.md", contents: readmeMd(ctx) },
    { path: "package.json", contents: packageJson() },
    { path: "tsconfig.json", contents: tsconfigJson() },
    { path: ".env.example", contents: envExample() },
    { path: "src/loadEnv.ts", contents: loadEnvTs() },
    { path: "src/server.ts", contents: serverTs() },
    { path: "src/woocommerce.ts", contents: woocommerceTs() },
  ];

  const changelog: ArtifactChangelog = {
    added: [
      "mcp-server/README.md — deploy guide and the payment boundary, in plain English",
      "mcp-server/package.json — Node project (@modelcontextprotocol/sdk + zod only)",
      "mcp-server/tsconfig.json — build config for `npm run build`",
      "mcp-server/.env.example — WOOCOMMERCE_STORE_URL placeholder (no API key needed)",
      "mcp-server/src/loadEnv.ts — loads .env before anything else (must be the first import in server.ts)",
      "mcp-server/src/server.ts — MCP tools: search_products, get_product, add_to_cart, update_cart_item, remove_cart_item, get_cart, begin_checkout",
      "mcp-server/src/woocommerce.ts — WooCommerce Store API client (catalog + cart only; no payment, no admin API)",
    ],
    corrected: [],
    must_complete: [
      "Deploy mcp-server/ to a host you control (Node 18+) — Adeptra does not host this for you.",
      "Set WOOCOMMERCE_STORE_URL in mcp-server/.env to your real store URL.",
      "Confirm your store exposes the WooCommerce Store API (GET /wp-json/wc/store/v1/products should return JSON) — standard on modern WooCommerce, but verify since some hosts/security plugins block wp-json routes.",
      "Once deployed, point your UCP manifest's dev.ucp.shopping service endpoint at this server's deployed URL and re-run analysis — that's what turns checkout/cart/catalog capability signals from \"enabled once deployed\" into a genuine pass.",
    ],
    flagged: [
      "Payment is intentionally NOT handled by this server — begin_checkout returns the cart summary and your store's normal checkout URL for handoff; this is a deliberate boundary, not a gap.",
    ],
  };

  return {
    artifact_type: "mcp_scaffold",
    target_url: TARGET_FOLDER,
    content: encodeFileTree(files),
    resolves_signal_keys: [], // never claimed resolved pre-deployment — see file header
    changelog,
  };
}
