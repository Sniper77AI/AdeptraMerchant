/**
 * Adeptra Merchant — WooCommerce MCP Shopping Server scaffold generator
 * (artifact_type = 'mcp_scaffold').
 *
 * PURE module: no network, no LLM, no DB. Emits a deployable Node/TypeScript
 * MCP server that makes a WooCommerce store agent-shoppable — catalog search
 * + cart building — with payment explicitly handed off to the merchant's own
 * checkout. Adeptra GENERATES and CONFIGURES; the merchant (or a future setup
 * service) DEPLOYS. Adeptra never hosts this server.
 *
 * PLATFORM-GATED, NOT PLATFORM-GUESSED: this generator only runs when
 * ctx.platform === "woocommerce" — an onboarding-declared fact (sites.platform),
 * never detected/guessed here. No confirmed platform, no scaffold: generating
 * a WooCommerce-specific server for a store we don't know is WooCommerce would
 * be exactly the kind of "looks-compliant-but-isn't" artifact this codebase
 * has repeatedly guarded against elsewhere (see manifestArtifact.ts's header).
 *
 * THE SAFE BOUNDARY IS ENFORCED BY API CHOICE, NOT JUST CONVENTION: every
 * generated call goes through WooCommerce's public, unauthenticated Store API
 * (/wp-json/wc/store/v1/*) for catalog reads and cart building ONLY. Nothing
 * generated here ever calls /wc/store/v1/checkout (payment) or any /wc/v3/
 * (admin — orders/customers/settings) route. begin_checkout returns the cart
 * summary + the store's own checkout URL for handoff; it does not process
 * payment. This boundary is asserted by a dedicated test (see test_artifacts.ts).
 *
 * MULTI-FILE OUTPUT: ArtifactDraft.content is one string, but a server is many
 * files. content holds a tagged, versioned JSON payload (see
 * artifacts/types.ts: ArtifactFileTree / encodeFileTree) rather than a single
 * file's text — reportBuilder.ts detects this by a stable content-shape tag
 * (not by artifact_type) and expands it into a mcp-server/ subfolder in the
 * exported zip. No DB migration: the file tree round-trips through the
 * existing `artifacts.content` text column unchanged.
 *
 * HONESTY: resolves_signal_keys is always empty. Generating a shopping server
 * that COULD make an agent's checkout/cart/catalog capability real is not the
 * same as it BEING real — that only happens once the server is deployed and
 * the UCP manifest's shopping endpoint points at it, which a future run's live
 * checks would then observe and score. Same principle as feedArtifact.ts's
 * supplemental feed resolving nothing pre-upload.
 */

