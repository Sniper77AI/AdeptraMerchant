function serverTs(): string {
  return `/**
 * Adeptra Merchant — WooCommerce MCP Shopping Server.
 *
 * Makes this store's catalog searchable and its cart buildable by an MCP-
 * speaking AI shopping agent. HARD BOUNDARY: this server never processes
 * payment. begin_checkout returns the current cart plus this store's normal
 * checkout URL — the agent/shopper hands off to your store's own checkout
 * from there. No /wc/store/v1/checkout call, no /wc/v3/ (admin) call, anywhere
 * in this file or woocommerce.ts.
 */

import "./loadEnv.js"; // MUST be the first import — see loadEnv.ts's header

import { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { StdioServerTransport } from "@modelcontextprotocol/sdk/server/stdio.js";
import { z } from "zod";
import { searchProducts, getProduct, getCart, addToCart, updateCartItem, removeCartItem } from "./woocommerce.js";

const STORE_URL = process.env.WOOCOMMERCE_STORE_URL;
if (!STORE_URL) {
  console.error("Set WOOCOMMERCE_STORE_URL in .env (see .env.example) before starting this server.");
  process.exit(1);
}
const CHECKOUT_PATH = process.env.WOOCOMMERCE_CHECKOUT_PATH || "/checkout/";

const server = new McpServer({ name: "woocommerce-shopping-server", version: "1.0.0" });

function textResult(data: unknown) {
  return { content: [{ type: "text" as const, text: JSON.stringify(data, null, 2) }] };
}

server.registerTool(
  "search_products",
  {
    title: "Search products",
    description: "Search this store's product catalog by keyword.",
    inputSchema: {
      query: z.string().describe("Search text, e.g. product name or keyword"),
      limit: z.number().int().min(1).max(50).optional(),
    },
  },
  async ({ query, limit }) => textResult(await searchProducts(query, limit ?? 10)),
);

server.registerTool(
  "get_product",
  {
    title: "Get product",
    description: "Get full details for one product by its numeric id.",
    inputSchema: { id: z.number().int() },
  },
  async ({ id }) => textResult(await getProduct(id)),
);

server.registerTool(
  "add_to_cart",
  {
    title: "Add to cart",
    description: "Add a product to the shopping cart.",
    inputSchema: { id: z.number().int(), quantity: z.number().int().min(1).default(1) },
  },
  async ({ id, quantity }) => textResult(await addToCart(id, quantity)),
);

server.registerTool(
  "update_cart_item",
  {
    title: "Update cart item",
    description: "Change the quantity of an existing cart line item.",
    inputSchema: {
      key: z.string().describe("The cart item's key, from get_cart/add_to_cart output"),
      quantity: z.number().int().min(1),
    },
  },
  async ({ key, quantity }) => textResult(await updateCartItem(key, quantity)),
);

server.registerTool(
  "remove_cart_item",
  {
    title: "Remove cart item",
    description: "Remove a line item from the cart.",
    inputSchema: { key: z.string().describe("The cart item's key, from get_cart/add_to_cart output") },
  },
  async ({ key }) => textResult(await removeCartItem(key)),
);

server.registerTool(
  "get_cart",
  {
    title: "Get cart",
    description: "Get the current cart contents and totals.",
    inputSchema: {},
  },
  async () => textResult(await getCart()),
);

server.registerTool(
  "begin_checkout",
  {
    title: "Begin checkout",
    description:
      "Returns the current cart summary and this store's normal checkout URL. Does NOT process payment — hand off to checkout_url for the shopper to complete their purchase on the store's own site.",
    inputSchema: {},
  },
  async () => {
    const cart = await getCart();
    return textResult({
      cart,
      checkout_url: \`\${STORE_URL!.replace(/\\/+$/, "")}\${CHECKOUT_PATH}\`,
      note: "Payment is not handled by this server — complete the purchase at checkout_url.",
    });
  },
);

const transport = new StdioServerTransport();
await server.connect(transport);
console.error("WooCommerce MCP shopping server running (stdio).");
`;
}

